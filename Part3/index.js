
let x = 10;
let y = 3;

console.log(x + y);//x + y is an expression(something that produces a value)
console.log(x - y);
console.log(x / y);
console.log(x % y);
console.log(x ** y);//exponentiation

//increment(++)
console.log(++x);
console.log(x++);
console.log(x);
//decrement(--)
console.log(--x);
console.log(x--);
console.log(x);
